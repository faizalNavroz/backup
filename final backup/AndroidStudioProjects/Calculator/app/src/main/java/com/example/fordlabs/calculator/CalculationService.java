package com.example.fordlabs.calculator;

public class CalculationService {

    public int addInputs(int a , int b){
        return a+b;
    }

    public int subtractInputs(int a , int b){
        return a-b;
    }
    public int multiplyInputs(int a , int b){
        return a*b;
    }
    public int divideInputs(int a , int b){
        return a/b;
    }

}
